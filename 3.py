
class Circle():
    def __init__(self, r):
        #declaring variable
        self.radius=r
        def area(self):
            return self.radius**3.141
        #returning area
        def perimeter(self):
            return 2*self.radius*3.141
        #Creating Obj
        NewCircle = Circle(7)
        print(NewCircle.area())
        print(NewCircle.perimeter())
        
